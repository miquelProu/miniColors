<template>
    <div class="column is-2"  @click="clicka()" :class="[!isMeu && isSelected ? 'selected' : '']">
        <div style="width: 100%;height: 30px;"
             :style="rellenu"
        >&nbsp;</div>
        <div style="background-color: white;">
            <div><span style="font-weight: bold;">{{col.referencia}}</span> {{col.esp}}</div>
            <div v-if="deltaColor">{{delta()}}</div>
        </div>
    </div>
</template>

<script>

import Chroma from "chroma-js";

export default {
    name: "color",
    props: {
        col: {
            required: true,
            type: Object,
        },
        isSelected: {
            required: false,
            type: Boolean,
            default: false
        },
        isMeu: {
            required: false,
            type: Boolean,
            default: false
        },
        deltaColor: {
            required: false,
            type: String
        }
    },
    data: function (){
        return {}
    },
    computed: {
        rellenu(){
            let base = {'background-color': this.col.color};

            if (this.col.color==='#FFFFFF'){
                base['border'] = '1px solid black';
            }
            return base;
        }
    },
    methods:{
        clicka(){
            this.$emit('selected', this.col);
        },
        delta(){
            return Chroma.deltaE(this.deltaColor, this.col.color);
        }
    },
    mounted() {

    }
}
</script>

